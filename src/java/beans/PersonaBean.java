package beans;

public class PersonaBean {
    private String curpPersona;
    private String apellidosPersona;
    private String nombrePersona;
    private boolean generoPersona;

    /**
     * @return the curpPersona
     */
    public String getCurpPersona() {
        return curpPersona;
    }

    /**
     * @param curpPersona the curpPersona to set
     */
    public void setCurpPersona(String curpPersona) {
        this.curpPersona = curpPersona;
    }

    /**
     * @return the apellidosPersona
     */
    public String getApellidosPersona() {
        return apellidosPersona;
    }

    /**
     * @param apellidosPersona the apellidosPersona to set
     */
    public void setApellidosPersona(String apellidosPersona) {
        this.apellidosPersona = apellidosPersona;
    }

    /**
     * @return the nombrePersona
     */
    public String getNombrePersona() {
        return nombrePersona;
    }

    /**
     * @param nombrePersona the nombrePersona to set
     */
    public void setNombrePersona(String nombrePersona) {
        this.nombrePersona = nombrePersona;
    }

    /**
     * @return the generoPersona
     */
    public boolean isGeneroPersona() {
        return generoPersona;
    }

    /**
     * @param generoPersona the generoPersona to set
     */
    public void setGeneroPersona(boolean generoPersona) {
        this.generoPersona = generoPersona;
    }
    
}
